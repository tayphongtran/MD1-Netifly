class Student {
    constructor(id,name, age) {
        this.id = id;
        this.name = name;
        this.age = age;
    }
    setId(id){
        this.id = id
    }
    getId(){
        return this.id
    }
    setName(name){
        this.name = name
    }
    getName(){
        return this.name
    }
    setAge(age){
        this.age = age
    }
    getAge(){
        return this.age
    }
}